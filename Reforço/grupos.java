public class grupos {
    private int quantidadeMembros;
    private String nomeGrupo;
    private String nacionalidade;
    private String QTAAlbuns;
    private int anoDeBut;
    private int numerosF;



    public int getQuantidadeMembros() {
        return quantidadeMembros;
    }

    public void setQuantidadeMembros(int quantidadeMembros) {
        this.quantidadeMembros = quantidadeMembros;
    }

    public String getNomeGrupo() {
        return nomeGrupo;
    }

    public void setNomeGrupo(String nomeGrupo) {
        this.nomeGrupo = nomeGrupo;
    }

    public String getNacionalidade() {
        return nacionalidade;
    }

    public void setNacionalidade(String nacionalidade) {
        this.nacionalidade = nacionalidade;
    }

    public String getQTAAlbuns() {
        return QTAAlbuns;
    }

    public void setQTAAlbuns(String QTAAlbuns) {
        this.QTAAlbuns = QTAAlbuns;
    }

    public int getAnoDeBut() {
        return anoDeBut;
    }

    public void setAnoDeBut(int anoDeBut) {
        this.anoDeBut = anoDeBut;
    }

    public int getNumerosF() {
        return numerosF;
    }

    public void setNumerosF(int numerosF) {
        this.numerosF = numerosF;
    }
}
